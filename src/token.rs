#[derive(Clone, Copy, Debug)]
pub struct Token<'a> {
    pub kind: TokenType,
    pub lexeme: &'a str,
    pub line: usize,
    pub col: usize,
}

#[derive(Clone, Copy, Debug, PartialEq)]
pub enum TokenType {
    RightBracket,
    LeftBracket,
    RightBrace,
    LeftBrace,
    RightParen,
    LeftParen,
    RightCaret,
    LeftCaret,
    Identifier,
    KIdentifier,
    Let,
    Ampersand,
    Plus,
    Minus,
    Star,
    Slash,
    If,
    For,
    In,
    Question,
    Dot,
    Semicolon,
    Colon,
    Comma,
    Struct,
    Number,
    String,
    Equal,
    Import,
    From,
    Export,
    Eof,
    Error,
}
